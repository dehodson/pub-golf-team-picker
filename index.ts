function randomChoice(array: any[]): any {
  const index = Math.floor(Math.random() * array.length);
  return array.splice(index, 1)[0];
}

function pairPeeps(peeps: string[]): string[][] {
  let people = [...peeps];
  let pairs = [];
  while (people.length > 0) {
    pairs.push([randomChoice(people), randomChoice(people)]);
  }
  return pairs;
}

function getContrastingColor(color: string): string {
  const matches = color.match(/rgb\((\d+), (\d+), (\d+)\)/);
  const [_, r, g, b] = matches;
  return ((+r) * 0.299 + (+g) * 0.587 + (+b) * 0.114) > 186
    ? '#000000'
    : '#FFFFFF';
}

function showTeam(members: string[], color: string, id: number): void {
  const teamCard = document.getElementById(`team${id}`);
  teamCard.style.backgroundColor = color;
  const contrastingColor = getContrastingColor(window.getComputedStyle(teamCard).backgroundColor);
  teamCard.style.color = contrastingColor;
  document.getElementById(`team${id}-member1`).innerText = members[0];
  document.getElementById(`team${id}-member2`).innerText = members[1];
  document.getElementById(`team${id}-teamcolor`).innerText = color;
}

